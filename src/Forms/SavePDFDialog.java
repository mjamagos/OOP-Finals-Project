    private void cbMarginKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cbMarginKeyReleased
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            Toolkit.getDefaultToolkit().beep();   
            //System.out.println("ENTER pressed");
            buttonPrint.doClick(500);
        }
    }//GEN-LAST:event_cbMarginKeyReleased

